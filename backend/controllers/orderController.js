// @desc    Update payment status
// @route   PATCH /api/orders/:id/payment
// @access  Private
exports.updatePaymentStatus = async (req, res) => {
  try {
    const { paymentStatus } = req.body;
    const validStatuses = ['pending', 'paid', 'cash_received', 'online_verified'];

    if (!validStatuses.includes(paymentStatus)) {
      return res.status(400).json({
        success: false,
        message: 'Invalid payment status. Use: pending, paid, cash_received, or online_verified'
      });
    }

    const order = await Order.findById(req.params.id);

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Order not found'
      });
    }

    order.paymentStatus = paymentStatus;
    await order.save();

    // Emit socket event
    if (req.io) {
      req.io.emit('orderUpdated', order);
      req.io.emit('paymentUpdated', {
        orderId: order._id,
        tokenNumber: order.tokenNumber,
        paymentStatus,
        paymentMethod: order.paymentMethod
      });
    }

    res.json({
      success: true,
      data: order
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message
    });
  }
};

// @desc    Get or regenerate payment QR for an order
// @route   GET /api/orders/:id/payment-qr
// @access  Public (customer needs this to pay)
exports.getPaymentQR = async (req, res) => {
  try {
    const order = await Order.findById(req.params.id)
      .select('orderNumber tokenNumber total paymentMethod paymentStatus customer.name');

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Order not found'
      });
    }

    // Don't generate QR if already paid
    if (['paid', 'cash_received', 'online_verified'].includes(order.paymentStatus)) {
      return res.json({
        success: true,
        data: order,
        paymentQR: null,
        message: 'Payment already completed'
      });
    }

    const paymentQR = await generatePaymentQR({
      amount: order.total,
      orderId: order.orderNumber,
      tokenNumber: order.tokenNumber,
    });

    res.json({
      success: true,
      data: order,
      paymentQR
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message
    });
  }
};

// @desc    Delete order
// @route   DELETE /api/orders/:id
// @access  Private/Admin
exports.deleteOrder = async (req, res) => {
  try {
    const order = await Order.findById(req.params.id);

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Order not found'
      });
    }

    await order.deleteOne();

    // Emit socket event
    if (req.io) {
      req.io.emit('orderDeleted', req.params.id);
    }

    res.json({
      success: true,
      message: 'Order deleted'
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message
    });
  }
};
